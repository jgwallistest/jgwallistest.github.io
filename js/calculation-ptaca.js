    {
    // Turn on the visibiliy of the 'Results' heading.

    document.getElementById('pressure-testing-results').style.display = "block";

    // Check that the results are valid numbers before displaying them. This is
    // necessary because in some circumstances calculations can produce results
    // that do not make sense, which a spreadsheet would display as '#DIV/0' or
    // '#NUM!' errors.

    if (isNaN(acaYValue))
      {
      document.getElementById('result-aca-y-value').innerHTML =
       'Calculated additional creep allowance is out of range (insufficient conditioning)';
      }
    else
      {
      document.getElementById('result-aca-y-value').innerHTML =
       'Additional creep allowance is ' + acaYValue.toFixed(2) +
       ' mbar (DO NOT CURRENTLY RELY ON THE ACCURACY OF THIS FIGURE)';
      }

    document.getElementById('result-max-x-value').innerHTML =
     'Maximum possible value for Test period / Conditioning period is ' + acaMaxXValue.toFixed(5);
    }

  //------------------------------------------------------------------------------
  // Determine the additional creep allowance by looking up the ratio of the test
  // period to the conditioning period in the appropriate lookup table. Note that
  // the answer can be NaN (i.e. Not a Number) to indicate that there is
  // 'insufficient conditioning'.
  //
  // We use linear search to find values in the lookup tables. This is the slowest
  // method possible, but we do not need efficiency because we only do the lookups
  // rarely. There is no point in using a smart search method as nobody would ever
  // notice the difference in speed. Plus it would make the code more complicated.
  //------------------------------------------------------------------------------

  function ACALookupY(acaCurveToUse, acaXValue)
    {
    var result = NaN;

    var lookupTableToUse;

    var allLookupTables =
      [
      curve1LookupTable,
      curve2LookupTable,
      curve3LookupTable,
      curve4LookupTable,
      curve5LookupTable,
      curve6LookupTable,
      curve7LookupTable,
      curve8LookupTable
      ];

    // First of all we need to find out which lookup table to use.

    lookupTableToUse = allLookupTables[acaCurveToUse];

    // Find out where the end of the lookup table is located.

    var lastIndex = lookupTableToUse.length - 1;

    // Find the last valid X value on the curve.

    var lastValidX = lookupTableToUse[lastIndex][0];

    // Check to see if the supplied X value, i.e. the ratio of the test period
    // to the conditioning period, is somewhere within the curve. If it is not
    // then there is no point in looking for it, and we leave the default
    // 'NaN' result as it is.

    if (acaXValue <= lastValidX)
      {
      for (var index = 0; index <= lastIndex; index = index + 1)
        {
        if (lookupTableToUse[index][0] >= acaXValue)
          {
          // The X value that we have found in the lookup table might be a bit
          // higher than the exact X value that we are looking for. Therefore
          // we should do some linear interpolation between the X value that
          // we have found and the preceding X value stored in the table to
          // increase the accuracy of the result.

          var higherX = lookupTableToUse[index][0];

          var higherY = lookupTableToUse[index][1];

          var lowerX = lookupTableToUse[index - 1][0];

          var lowerY = lookupTableToUse[index - 1][1];

          var xGap = higherX - lowerX;

          var yGap = higherY - lowerY;

          var proportion = (acaXValue - lowerX) * xGap;

          result = lowerY + (proportion * yGap);

          break;
          }
        }
      }

    return result;
    }

