  //----------------------------------------------------------------------------
  // Fetch the input values and return 'true' if they are all valid.
  //----------------------------------------------------------------------------

  function FetchInputValues()
    {
    // Throughout the following code, 'NaN' means 'not a number'.

    // We return 'false' immediately on finding a problem. This means that the
    // function contains multiple 'return' statements, which is not usually a
    // good idea, but in this case the code is short and the logic obvious.

    // The additional creep allowance curve to use is chosen from a drop-down
    // list, which contains all valid combinations of circumstances. We just
    // fetch the index of the chosen item.

    acaCurveToUse =
     document.getElementById('aca-curve-to-use').selectedIndex;

    // The ratio of the test period to the conditioning period is the X value.

    acaXValue =
     parseFloat(document.getElementById('aca-x-value').value);

    if (isNaN(acaXValue) || (acaXValue <= 0))
      {
      alert('The ratio of the test period to the conditioning period must be a positive number');
      return false;
      }

    return true;
    }

  //----------------------------------------------------------------------------
  // Do the pressure test additional creep allowance calculation.
  //----------------------------------------------------------------------------

  function DoCalculation()
    {
    // Find the additional creep allowance, i.e. the Y value, by looking up the
    // ratio of the test period to the conditioning period, i.e. the X value,
    // in the appropriate lookup table. There are eight lookup tables, one
    // for each additional creep allowance curve.

    acaYValue = ACALookupY(acaCurveToUse, acaXValue);

    // Find the maximum possible ratio of the test period to the conditioning
    // period.

    acaMaxXValue = ACALookupMaxX(acaCurveToUse);
    }

  //----------------------------------------------------------------------------
  // Display the results.
